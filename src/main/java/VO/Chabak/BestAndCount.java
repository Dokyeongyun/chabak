package VO.Chabak;

public class BestAndCount {
    private String cityProvince;
    private String bestPlaceName;
    private String bestPlaceImage;
    private int count;
    private int numOfJJIM;

    public BestAndCount() { }

    public String getCityProvince() { return cityProvince; }
    public String getBestPlaceName() { return bestPlaceName; }
    public String getBestPlaceImage() { return bestPlaceImage; }
    public int getCount() { return count; }
    public int getNumOfJJIM() { return numOfJJIM; }

    public void setCityProvince(String cityProvince) { this.cityProvince = cityProvince; }
    public void setBestPlaceName(String bestPlaceName) { this.bestPlaceName = bestPlaceName; }
    public void setBestPlaceImage(String bestPlaceImage) { this.bestPlaceImage = bestPlaceImage; }
    public void setCount(int count) { this.count = count; }
    public void setNumOfJJIM(int numOfJJIM) { this.numOfJJIM = numOfJJIM; }

}
